package dev.gab.kickstart.aula11;

public class Main {

    public static void main(String[] args) {

        System.out.println(3 + 2); // soma

        System.out.println(5 - 1); // subtração

        System.out.println(3 * 2); // multiplicação

        System.out.println(4 / 2); // divisão

        System.out.println(4 % 2); // Módulo

        int n1 =  5;
        System.out.println(++n1);
        System.out.println(n1);  // incremento

        System.out.println(n1--);
        System.out.println(n1); // decremento
    }
}
